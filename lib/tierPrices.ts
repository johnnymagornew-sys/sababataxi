export type TierIndex = 0 | 1 | 2 | 3 | 4 | 5

export function getTierIndex(passengers: number): TierIndex {
  if (passengers <= 4)  return 0
  if (passengers <= 6)  return 1
  if (passengers <= 8)  return 2
  if (passengers <= 10) return 3
  if (passengers <= 14) return 4
  return 5
}

