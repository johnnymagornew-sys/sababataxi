/**
 * Get the base price for a city + passenger count.
 * Falls back to multiplier-based estimate if city not in table.
 */
export function getTierBasePrice(city: string, passengers: number, fallbackBasePrice: number): number {
  const row = TIER_PRICES[city]
  const idx = getTierIndex(passengers)
  if (row) return row[idx]
  // Fallback multipliers for unknown cities
  const fallbacks: Record<TierIndex, number> = { 0: 1.0, 1: 1.65, 2: 1.95, 3: 2.2, 4: 2.6, 5: 2.8 }
  return Math.round((fallbackBasePrice * fallbacks[idx]) / 5) * 5
}
